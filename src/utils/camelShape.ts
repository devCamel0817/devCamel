import type { Point } from '../utils/fourier';

// 낙타 모양 프리셋 (CamelLogo의 외곽선 단순화)
export function camelShape(scale = 110, offsetX = 0, offsetY = 30): Point[] {
  // 더 부드럽고 디테일한 낙타 외곽선 (이미지 참고, 38점)
  const raw: [number, number][] = [
    [32, 170], [36, 155], [42, 140], [54, 125], [62, 115], // 꼬리~엉덩이~뒷다리
    [66, 140], [70, 170], [80, 170], [78, 140], [82, 120], // 뒷다리~배
    [95, 110], [110, 108], [115, 130], [120, 170], [130, 170], // 앞다리
    [128, 130], [132, 110], [145, 105], [160, 100], // 가슴~목 아래
    [170, 80], [180, 65], [192, 60], [205, 55], [218, 50], // 목~머리~코
    [225, 45], [220, 38], [210, 36], [200, 38], [190, 45], // 머리 윗부분~귀
    [180, 55], [170, 60], [160, 65], [150, 70], [140, 80], // 목~등 시작
    [130, 90], [120, 100], [110, 105], [90, 110], [70, 120], [55, 135], [40, 155], [32, 170] // 등~배~꼬리로 마감
  ];
  return raw.map(([x, y]) => ({
    x: ((x - 125) / 110) * scale + offsetX,
    y: ((y - 125) / 110) * scale + offsetY,
  }));
}
